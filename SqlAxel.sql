CREATE TABLE tblDepartamentos(
	Dpt_DepartamentoId		INT NOT NULL,
	Dpt_Descripcion			NVARCHAR(255) NOT NULL,
	Dpt_UsuarioCreacionId		INT NOT NULL,
	Dpt_FechaCreacion			DATE NOT NULL,
	Dpt_UsuarioModificacionId	INT,
	Dpt_FechaModificacion		DATE
	CONSTRAINT PK_dbo_tblDepartamentos_Dpto_DepartamentoId PRIMARY KEY(Dpt_DepartamentoId)
);



CREATE TABLE tblCiudades(
	Ciu_CiudadId				INT,
	Ciu_Descripcion				NVARCHAR(255) NOT NULL,
	Dpt_DepartamentoId			INT NOT NULL,
	Ciu_UsuarioCreacionId		INT NOT NULL,
	Ciu_FechaCreacion			DATE NOT NULL,
	Ciu_UsuarioModificacionId	INT,
	Ciu_FechaModificacion		DATE ,
	CONSTRAINT PK_db_tblCiudades_Ciu_CiudadId PRIMARY KEY(Ciu_CiudadId),
	CONSTRAINT FK_dbo_tblCiudades_dbo_tblDepartamentos_Dpto_DepartamentoId1 FOREIGN KEY(Dpt_DepartamentoId) REFERENCES tblDepartamentos(Dpt_DepartamentoId)
);


CREATE TABLE tblGrados(
    grd_ID				INT IDENTITY(1,1),
    grd_Descripcion		NVARCHAR(200),
    grd_UsuCrea			INT NOT NULL,
    grd_FechaCrea		DATE NOT NULL,
    grd_UsuMod			INT,
    grd_FechaMod		DATE,
	grd_Estado			BIT
    CONSTRAINT PK_tblGrados_grd_ID PRIMARY KEY(grd_ID)
);


CREATE TABLE tblEstadosCiviles(
    etc_ID				CHAR(1) NOT NULL,
    etc_Descripcion		NVARCHAR(100),
    etc_UsuCrea			INT NOT NULL,
    etc_FechaCrea		DATE NOT NULL,
    etc_UsuMod			INT,
    etc_FechaMod        DATE
    CONSTRAINT PK_tblEstadoCivil_etc_ID PRIMARY KEY(etc_ID),
    CONSTRAINT CK_tblEstadoCivil_etc_ID CHECK(etc_ID IN ('C', 'D', 'S', 'U', 'V'))
);


CREATE TABLE tblJornada(
	jrn_Id					CHAR(1),
	jrn_Descripcion			NVARCHAR(150),
	jrn_UsuarioCreacion		INT,
	jrn_UsuarioModificacion	INT,
	jrn_FechaCreacion		DATETIME,
	jrn_FechaModificacion	DATETIME,
	jrn_Estado				BIT,
	CONSTRAINT PK_dbo_tblJornada_jrn_id PRIMARY KEY (jrn_id)
)

create table tblSeccion(
	scn_Id					CHAR(1),
	scn_Descripcion			NVARCHAR(150),
	scn_UsuarioCreacion		INT,
	scn_UsuarioModificacion INT,
	scn_FechaCreacion		DATETIME,
	scn_FechaModificacion	DATETIME,
	scn_Estado				BIT,
	CONSTRAINT PK_dbo_tblSeccion_scn_id PRIMARY KEY (scn_id)
)
	

CREATE TABLE tblCargos(
	Crg_CargoID		INT IDENTITY(1,1),
	Crg_Cargo		NVARCHAR(255) NOT NULL,
	Crg_UsuCrea     INT NOT NULL,
    Crg_FechaCrea	DATE NOT NULL,
    Crg_UsuMod      INT,
    Crg_FechaMod    DATE
	CONSTRAINT PK_dbo_tblCargos_Crg_CargoID PRIMARY KEY(Crg_CargoID)
);


CREATE TABLE tblPersonas(
	per_Id						INT IDENTITY(1,1),
	per_Nombre					NVARCHAR(150)NOT NULL,
	per_Apellido				NVARCHAR (150) NOT NULL,
	per_Fechanacimiento			DATE NOT NULL,
	per_Identidad				NVARCHAR (150) NOT NULL,
	Ciu_CiudadId				INT NOT NULL,
	per_Sexo					CHAR(1)NOT NULL,
	etc_ID						CHAR(1)NOT NULL,
	per_Telefono				NVARCHAR(150)NOT NULL,
	Crg_CargoID					INT NOT NULL,
	per_EncargadoId				INT,
	per_UsuCrea					INT NOT NULL,
    per_FechaCrea				DATE NOT NULL,
    per_UsuMod					INT,
    per_FechaMod				DATE,
	per_Estado					BIT NOT NULL
CONSTRAINT PK_dbo_tblPersonas_per_Id PRIMARY KEY(per_Id),
CONSTRAINT FK_dbo_tblPersonas_Ciu_CiudadId_tblCiudades_tblCiudades FOREIGN KEY(Ciu_CiudadId) REFERENCES tblCiudades(Ciu_CiudadId),
CONSTRAINT CK_dbo_tblPersonas_per_Sexo CHECK(per_Sexo IN ('F', 'M')),
CONSTRAINT FK_dbo_tblPersonas_etc_ID_tblEstadosCiviles_etc_ID FOREIGN KEY(etc_ID) REFERENCES  tblEstadosCiviles(etc_ID),
CONSTRAINT FK_dbo_tblPersonas_Crg_CargoID_tblCargos_Crg_CargoID FOREIGN KEY(Crg_CargoID) REFERENCES tblCargos(Crg_CargoID)
);


CREATE TABLE tblUsuarios(
	Usu_UsuarioID	INT IDENTITY( 1,1),
	Usu_Usuario		NVARCHAR(255),
	Usu_Clave		NVARCHAR(MAX),
	per_Id			INT NOT NULL,
	Usu_EsAdmin		BIT,
	Usu_UsuCrea		INT,
	Usu_FechaCrea	DATE,
	Usu_UsuMod		INT, 
	Usu_FechaMod	DATE,
	Usu_Estado		BIT
	CONSTRAINT FK_dbo_tblUsuarios_dbo_tblPersonas_per_Id FOREIGN KEY(per_Id) REFERENCES tblPersonas(per_Id)	
);


CREATE TABLE tblMatricula(
	mtr_ID				INT IDENTITY(1,1),
	per_Id				INT NOT NULL,
	mtr_Fecha			DATE NOT NULL,
	grd_ID				int,
	jrn_Id				char(1),
	scn_Id				char(1),
	mtr_UsuarioCre		INT NOT NULL,
	mtr_FechaCrea		DATE NOT NULL,
	mtr_UsuarioMod		INT,
	mtr_FechaMod		DATE,
	mtr_Estado			BIT,
	CONSTRAINT PK_dbo_tblMatricula_mtr_ID PRIMARY KEY (mtr_ID),
	CONSTRAINT FK_dbo_tblMatricula_per_Id_ID_tblPersonas_per_Id FOREIGN KEY(per_Id) REFERENCES tblPersonas(per_Id),
	CONSTRAINT FK_dbo_tblMatricula_mtr_Jornada_tblJornada_jrn_id FOREIGN KEY(jrn_Id) references tblJornada(jrn_id),
	CONSTRAINT FK_dbo_tblMatricula_mtr_Seccion_tblSeccion_scn_id FOREIGN KEY (scn_Id) REFERENCES tblSeccion(scn_id),
	CONSTRAINT FK_dbo_tblMatricula_mtr_Grado_tblgrdo_grd_ID FOREIGN KEY (grd_ID) REFERENCES tblGrados(grd_ID)
);





